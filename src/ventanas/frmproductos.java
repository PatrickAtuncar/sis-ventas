    private void tblistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblistadoMouseClicked
        int fila = tblistado.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila de la tabla");
        } else {
            id = Integer.parseInt(tblistado.getValueAt(fila, 0).toString());
            String nombre = tblistado.getValueAt(fila, 1).toString();
            Double precio = Double.parseDouble(tblistado.getValueAt(fila, 2).toString());
            int stock = Integer.parseInt(tblistado.getValueAt(fila, 3).toString());
            String estado = tblistado.getValueAt(fila, 4).toString();

            txtnombre.setText(nombre);
            txtprecio.setText(String.valueOf(precio));
            txtstock.setText(String.valueOf(stock));
            txtestado.setSelectedItem(estado);
        }
    }//GEN-LAST:event_tblistadoMouseClicked


