    void listar() {
        List<producto> lista = pf.listar();
        modelo = (DefaultTableModel) tblistado.getModel();
        Object[] ob = new Object[5];

        for (int i = 0; i < lista.size(); i++) {
            ob[0] = lista.get(i).getIdproducto();
            ob[1] = lista.get(i).getNombre();
            ob[2] = lista.get(i).getPrecio();
            ob[3] = lista.get(i).getStock();
            ob[4] = lista.get(i).getEstado();
            modelo.addRow(ob);
        }
        tblistado.setModel(modelo);
    }
    
    void agregar() {
        String nombre = txtnombre.getText();
        String precio = txtprecio.getText();
        String stock = txtstock.getText();
        String estado = txtestado.getSelectedItem().toString();

        if (estado.equals("SELECCIONAR") || precio.equals("") || nombre.equals("") || stock.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe completar todos los datos");
            txtestado.setSelectedIndex(0);
            return;
        }
        Object[] ob = new Object[4];
        ob[0] = nombre;
        ob[1] = precio;
        ob[2] = stock;
        ob[3] = estado;
        pf.add(ob);
    }
    
    void actualizar() {
        int fila = tblistado.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila de la tabla");
        } else {
            String nombre = txtnombre.getText();
            String precio = txtprecio.getText();
            String stock = txtstock.getText();
            String estado = txtestado.getSelectedItem().toString();
            Object[] ob = new Object[5];
            ob[0] = nombre;
            ob[1] = precio;
            ob[2] = stock;
            ob[3] = estado;
            ob[4] = id;

            pf.actualizar(ob);
        }
    }
    
    void eliminar() {
        int fila = tblistado.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "debe seleccionar la fila a eliminar");
        } else {
            int ids = Integer.parseInt(tblistado.getValueAt(fila, 0).toString());
            pf.eliminar(ids);
        }
    }

    void nuevo() {
        txtnombre.setText("");
        txtprecio.setText("");
        txtstock.setText("");
        txtestado.setSelectedIndex(0);
        txtnombre.requestFocus();
    }

    void limpiarListado() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

